let currentLang = localStorage.getItem(LS_LANG_KEY) || "ar";

export function setLang(lang) {
  currentLang = DICTS[lang] ? lang : "ar";
  localStorage.setItem(LS_LANG_KEY, currentLang);
}
export function getLang() {
  return localStorage.getItem(LS_LANG_KEY) || currentLang || "ar";
}
export function t(key) {
  const d = DICTS[currentLang] || DICTS.en;
  return (key in d ? d[key] : (DICTS.en[key] ?? key));
}
export function tn(key, params = {}) {
  return t(key).replace(/\{(\w+)\}/g, (_, k) => (params[k] ?? ""));
}
export function dirForLang(lang) {
  return lang === "ar" ? "rtl" : "ltr";
}
export function locale() {
  return currentLang === "ar" ? "ar-SA" : "en";
}
export function fmtCurrency(n, code = "SAR") {
  const loc = locale();
  return new Intl.NumberFormat(loc, {
    style: "currency",
    currency: code,
    currencyDisplay: currentLang === "en" ? "code" : "symbol",
    maximumFractionDigits: 0
  }).format(Number(n) || 0);
}

export function fmtSAR(n) {
  return fmtCurrency(n, "SAR");
}

export function fmtDate(ts) {
  return new Date(ts).toLocaleString(locale());
}

export function formatTimeAgo(timestamp) {
  const now = Date.now();
  const diff = now - timestamp;
  const minutes = Math.floor(diff / 60000);
  const hours = Math.floor(diff / 3600000);
  const days = Math.floor(diff / 86400000);
  
  if (minutes < 1) return t("just_now") || "Just now";
  if (minutes < 60) return `${minutes}${t("min_ago") || "m"}`;
  if (hours < 24) return `${hours}${t("hour_ago") || "h"}`;
  if (days < 7) return `${days}${t("day_ago") || "d"}`;
  
  return fmtDate(timestamp);
}

